def course_summary(activities_df: pd.DataFrame) -> pd.DataFrame:
    rows = []
    for course in sorted(activities_df["course"].dropna().unique()):
        cdf = activities_df[activities_df["course"] == course]
        total     = len(cdf)
        completed = (cdf["status"] == "Completed").sum()
        students  = cdf["email"].nunique()
        internal  = cdf.drop_duplicates("email").query("student_type == 'Internal'").shape[0]
        external  = cdf.drop_duplicates("email").query("student_type == 'External'").shape[0]
        inactive  = (
            cdf.groupby("email")["status"]
            .apply(lambda x: (x == "Completed").sum() == 0)
            .sum()
        )
        rate = round(100 * completed / total, 1) if total else 0
        rows.append({
            "Course":            course,
            "Students":          students,
            "Completion Rate":   f"{rate}%",
            "Internal":          internal,
            "External":          external,
            "Inactive Students": inactive,
        })
    return pd.DataFrame(rows)


def activity_breakdown(activities_df: pd.DataFrame, course: str) -> pd.DataFrame:
    cdf = activities_df[activities_df["course"] == course]
    result = (
        cdf.groupby(["activity_category", "activity_name"])
        .agg(
            students  = ("email", "nunique"),
            completed = ("status", lambda x: (x == "Completed").sum()),
        )
        .reset_index()
    )
    result["completion_%"] = (
        100 * result["completed"] / result["students"]
    ).round(1).astype(str) + "%"
    return result.sort_values(["activity_category", "completion_%"], ascending=[True, False])


def at_risk_students(activities_df: pd.DataFrame) -> pd.DataFrame:
    exclude = {"Certificate", "Feedback", "Misc", "Other"}
    core = activities_df[~activities_df["activity_category"].isin(exclude)]
    prog = (
        core.groupby(["email", "student_name", "course"])
        .agg(total=("activity_name", "count"), done=("status", lambda x: (x == "Completed").sum()))
        .reset_index()
    )
    prog["rate"] = (100 * prog["done"] / prog["total"]).round(1)
    return (
        prog[(prog["done"] > 0) & (prog["rate"] < 80)]
        .sort_values("rate")
        .head(30)
    )


