# ---------------------------------------------------------------------------
# Unicode sanitiser — fpdf2 Helvetica only supports latin-1 range
# ---------------------------------------------------------------------------

def s(text: str) -> str:
    """Strips characters outside latin-1 range to prevent Helvetica font errors."""
    return str(text).encode("latin-1", errors="replace").decode("latin-1")


# ---------------------------------------------------------------------------
# Colours (matching dashboard theme)
# ---------------------------------------------------------------------------
NEST_BLUE  = (31, 60, 136)    # #1f3c88
NEST_RED   = (230, 57, 70)    # #e63946
LIGHT_GRAY = (245, 245, 245)
MID_GRAY   = (180, 180, 180)
WHITE      = (255, 255, 255)
DARK_TEXT  = (30, 30, 30)


# ---------------------------------------------------------------------------
# PDF class with helpers
# ---------------------------------------------------------------------------

class NeSTPDF(FPDF):

    def header(self):
        # Blue top bar
        self.set_fill_color(*NEST_BLUE)
        self.rect(0, 0, 210, 12, "F")
        self.set_font("Helvetica", "B", 9)
        self.set_text_color(*WHITE)
        self.set_xy(10, 2)
        self.cell(0, 8, "NeST Digital Academy - L&D Analytics Report", align="L")
        self.set_xy(0, 2)
        self.cell(200, 8, f"Page {self.page_no()}", align="R")
        self.ln(10)

    def footer(self):
        self.set_y(-12)
        self.set_font("Helvetica", "", 7)
        self.set_text_color(*MID_GRAY)
        self.cell(0, 8, "Generated by NeST Dashboard · Confidential", align="C")

    def section_title(self, text: str):
        self.ln(4)
        self.set_fill_color(*NEST_BLUE)
        self.set_text_color(*WHITE)
        self.set_font("Helvetica", "B", 11)
        self.cell(0, 8, f"  {text}", fill=True, ln=True)
        self.ln(3)

    def sub_title(self, text: str):
        self.set_text_color(*NEST_BLUE)
        self.set_font("Helvetica", "B", 10)
        self.cell(0, 6, text, ln=True)
        self.set_text_color(*DARK_TEXT)
        self.ln(1)

    def body_text(self, text: str):
        self.set_font("Helvetica", "", 9)
        self.set_text_color(*DARK_TEXT)
        self.multi_cell(0, 5, text)
        self.ln(1)

    def stat_row(self, label: str, value: str, shade: bool = False):
        if shade:
            self.set_fill_color(*LIGHT_GRAY)
        else:
            self.set_fill_color(*WHITE)
        self.set_font("Helvetica", "B", 9)
        self.set_text_color(*DARK_TEXT)
        self.cell(80, 7, f"  {label}", fill=True, border=1)
        self.set_font("Helvetica", "", 9)
        self.cell(110, 7, f"  {value}", fill=True, border=1, ln=True)

    def table_header(self, cols: list, widths: list):
        self.set_fill_color(*NEST_BLUE)
        self.set_text_color(*WHITE)
        self.set_font("Helvetica", "B", 8)
        for col, w in zip(cols, widths):
            self.cell(w, 7, f" {col}", fill=True, border=1)
        self.ln()
        self.set_text_color(*DARK_TEXT)

    def table_row(self, values: list, widths: list, shade: bool = False):
        if shade:
            self.set_fill_color(*LIGHT_GRAY)
        else:
            self.set_fill_color(*WHITE)
        self.set_font("Helvetica", "", 8)
        for val, w in zip(values, widths):
            self.cell(w, 6, f" {str(val)}", fill=True, border=1)
        self.ln()


# ---------------------------------------------------------------------------
# Chart → PNG bytes using kaleido
# ---------------------------------------------------------------------------

def fig_to_png(fig: go.Figure, width: int = 700, height: int = 350) -> Optional[bytes]:
    """Converts a Plotly figure to PNG bytes via kaleido."""
    try:
        return fig.to_image(format="png", width=width, height=height, scale=2)
    except Exception as e:
        print(f"[exporter] Warning: could not render chart - {e}")
        return None


def add_chart_to_pdf(pdf: NeSTPDF, fig: go.Figure, title: str = "", width_mm: int = 180):
    """Renders a Plotly figure and embeds it in the PDF."""
    png_bytes = fig_to_png(fig)
    if png_bytes is None:
        pdf.body_text(f"[Chart unavailable: {title}]")
        return

    with tempfile.NamedTemporaryFile(suffix=".png", delete=False) as tmp:
        tmp.write(png_bytes)
        tmp_path = tmp.name

    try:
        if title:
            pdf.sub_title(title)
        # Centre the image on the page
        x = (210 - width_mm) / 2
        # Height proportional to 700x350 aspect ratio
        height_mm = width_mm * 350 / 700
        # Page break if not enough space
        if pdf.get_y() + height_mm > 270:
            pdf.add_page()
        pdf.image(tmp_path, x=x, w=width_mm)
        pdf.ln(3)
    finally:
        os.unlink(tmp_path)


